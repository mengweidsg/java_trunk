package com.esunny.satellite.vo;

import java.io.Serializable;

/**
 * 修改密码VO
 * 
 * @author Jet Xu 2012-5-2 上午10:53:03
 */
public class EditPasswordVo implements Serializable {

    private static final long serialVersionUID = -7320526048625848114L;

    private Long              id;

    /**
     * 修改前密码
     */
    private String            oldPwd;

    /**
     * 修改后密码
     */
    private String            newPwd;

    /**
     * 修改后密码（确认）
     */
    private String            newPwdAgain;

    /**
     * @return the id
     */
    public Long getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(Long id) {
        this.id = id;
    }

    /**
     * @return the oldPwd
     */
    public String getOldPwd() {
        return oldPwd;
    }

    /**
     * @param oldPwd the oldPwd to set
     */
    public void setOldPwd(String oldPwd) {
        this.oldPwd = oldPwd;
    }

    /**
     * @return the newPwd
     */
    public String getNewPwd() {
        return newPwd;
    }

    /**
     * @param newPwd the newPwd to set
     */
    public void setNewPwd(String newPwd) {
        this.newPwd = newPwd;
    }

    /**
     * @return the newPwdAgain
     */
    public String getNewPwdAgain() {
        return newPwdAgain;
    }

    /**
     * @param newPwdAgain the newPwdAgain to set
     */
    public void setNewPwdAgain(String newPwdAgain) {
        this.newPwdAgain = newPwdAgain;
    }

}
